#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t g;

	i = 0;
	g = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[g] != '\0' && g < n)
	{
		s1[i] = s2[g];
		i++;
		g++;
	}
	s1[i] = '\0';
	return (s1);
}
