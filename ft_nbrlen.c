#include "printf.h"

size_t	ft_nbrlen(intmax_t n)
{
	size_t len;

	if (n == 0)
		return (1);
	len = 0;
	if (n < 0)
		++len;
	while (n)
	{
		++len;
		n /= 10;
	}
	return (len);
}
